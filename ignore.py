def get_remained_heads(series_complete=[], series_to_exclude=[]):
    if series_complete == []:
        complete_series = set([f"L{i}H{j}" for i in range(30) for j in range(12)])
    else:
        complete_series = set()
        for series in series_complete:
            complete_series.update(series.strip(',').split(","))
    remained_series = complete_series.copy()
    for series in series_to_exclude:
        remained_series -= set(series.strip(',').split(","))
    
    remained_series = sorted(remained_series, key=sort_key)
    return ','.join(remained_series)

def find_missed_heads(series_complete=[], sub_series=[]):

    if series_complete == []:
        complete_series = set([f"L{i}H{j}" for i in range(30) for j in range(12)])
    else:
        complete_series = set()
        for series in series_complete:
            complete_series.update(series.strip(',').split(","))

    exclude_sets = []
    for s in sub_series:
        head_set = set(s.strip(',').split(","))
        exclude_sets.append(head_set)

    # 1. find missed head
    for head_set in exclude_sets:
        complete_series -= head_set
    complete_series = sorted(complete_series, key=sort_key)
    print(complete_series)

    # 2. ensure that there is no intersection among the series in `series_to_exclude`
    for i in range(len(exclude_sets)):
        for j in range(i + 1, len(exclude_sets)):
            set1 = exclude_sets[i]
            set2 = exclude_sets[j]
            
            # 求交集
            intersection = set1 & set2
            if intersection:
                # 发现交集，抛出明确错误
                raise ValueError(
                    f"series_to_exclude 中第 {i} 项和第 {j} 项存在重复的 head：\n"
                    f"{sorted(intersection)}"
                )

    return complete_series

